// Exercise 4 - Print numbers from 1 to 5 using while loop

public class Ejercicio4_ContadorWhile {

    public static void main(String[] args) {
        int i = 1; // Variable inicial

        // Ciclo while que se ejecuta mientras i <= 5
        while (i <= 5) {
            System.out.println("i");
            i++; // Incrementamos i para evitar ciclo infinito
        }
    }
}
