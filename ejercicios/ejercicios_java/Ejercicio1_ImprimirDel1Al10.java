// Ejercicio 1 - Imprimir del 1 al 10
// Exerecise 1 - Print numbers fron 1 to 10

public class Ejercicio1_ImprimirDel1Al10 {

    public static void main(String[] args) {
        // Usamos un ciclo for que inicia en 1 y termina en 10
        // We use for loop that starts at 1 and ends at 10

        for (int i = 1; i <= 10; i++) {
            System.out.println(i); // Imprime el valor de i en cada vuelta
        }
    }
}